#include "meta/headers.hpp"


/***********************************************************************
     * Cabinet
     * constructor

***********************************************************************/
ach::Cabinet::Cabinet()
{
	game = ach::Arcade::create();
}



/***********************************************************************
     * Cabinet
     * destructor

***********************************************************************/
ach::Cabinet::~Cabinet()
{
	delete game;
}



/***********************************************************************
     * Cabinet
     * update

***********************************************************************/
void ach::Cabinet::update()
{
	game->controls();
	game->update();
}



/***********************************************************************
     * Cabinet
     * render

***********************************************************************/
void ach::Cabinet::render()
{
	game->render();
}



/***********************************************************************
     * Cabinet
     * init

***********************************************************************/
void ach::Cabinet::init()
{
	delete game;

	game = ach::Arcade::create();
}



/***********************************************************************
     * Cabinet
     * active

***********************************************************************/
bool ach::Cabinet::active()
{
	if (!game)
		return false;

	return game->active;
}
