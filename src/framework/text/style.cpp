#include "meta/headers.hpp"


/***********************************************************************
     * TextStyle
     * constructor

***********************************************************************/
ach::TextStyle::TextStyle()
{
	font = NULL;
}



/***********************************************************************
     * TextStyle
     * destructor

***********************************************************************/
ach::TextStyle::~TextStyle()
{
}



/***********************************************************************
     * TextStyle
     * spacing

***********************************************************************/
int ach::TextStyle::spacing()
{
	if (!font)
		return 0;

	return font->getLineSpacing(size);
}
