#include "meta/headers.hpp"


/***********************************************************************
     * Score
     * constructor

***********************************************************************/
ach::Score::Score()
{
	reset();
}



/***********************************************************************
     * Score
     * destructor

***********************************************************************/
ach::Score::~Score()
{
}



/***********************************************************************
     * Score
     * reset

***********************************************************************/
void ach::Score::reset()
{
	set(0);
}



/***********************************************************************
     * Score
     * check

***********************************************************************/
bool ach::Score::check()
{
	return sum() == csum;
}



/***********************************************************************
     * Score
     * increase

***********************************************************************/
void ach::Score::increase()
{
	add(1);
}



/***********************************************************************
     * Score
     * add

***********************************************************************/
void ach::Score::add(int score)
{
	set(check() ? value + score : 0);
}



/***********************************************************************
     * Score
     * set

***********************************************************************/
void ach::Score::set(int score)
{
	value = score;
	csum  = sum();
}



/***********************************************************************
     * Score
     * sum

***********************************************************************/
int ach::Score::sum()
{
	return ~value;
}



/***********************************************************************
     * Score
     * get

***********************************************************************/
int ach::Score::get()
{
	if (!check())
		return 0;

	return value;
}
