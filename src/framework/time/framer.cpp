#include "meta/headers.hpp"


/***********************************************************************
     * Framer
     * constructor

***********************************************************************/
ach::Framer::Framer()
{
	set(TIME_ANIMATION_DEFAULT);
}



/***********************************************************************
     * Framer
     * destructor

***********************************************************************/
ach::Framer::~Framer()
{
}



/***********************************************************************
     * Framer
     * set

***********************************************************************/
void ach::Framer::set(float _timer)
{
	timer = _timer;
	value = _timer;
}



/***********************************************************************
     * Framer
     * reset

***********************************************************************/
void ach::Framer::reset()
{
	value = timer;
}



/***********************************************************************
     * Framer
     * zero

***********************************************************************/
void ach::Framer::zero()
{
	value = 0.0f;
}



/***********************************************************************
     * Framer
     * update

***********************************************************************/
int ach::Framer::update(bool real)
{
	int res = 0;

	value -= tm->get(real);

	while (value < 0.0f)
	{
		value += timer;
		res++;
	}

	return res;
}
