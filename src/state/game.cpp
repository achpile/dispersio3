#include "meta/headers.hpp"


/***********************************************************************
     * StateGame
     * constructor

***********************************************************************/
ach::StateGame::StateGame()
{
	proc = new ach::ProcessWorld(this);

	app->mouse(false);
}



/***********************************************************************
     * StateGame
     * destructor

***********************************************************************/
ach::StateGame::~StateGame()
{
	delete proc;
}



/***********************************************************************
     * StateGame
     * update

***********************************************************************/
void ach::StateGame::update()
{
	proc->update();
}



/***********************************************************************
     * StateGame
     * event

***********************************************************************/
void ach::StateGame::event(sf::Event e)
{
	proc->event(e);
}



/***********************************************************************
     * StateGame
     * next

***********************************************************************/
void ach::StateGame::next()
{
	app->stateSet(ach::GameState::gsMenu);
}
